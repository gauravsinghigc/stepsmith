                                                   <h1>Canada – Study Visa</h1>
                                   
<h5>Canada Student Visa From India</h5>
<p>Canada – a country famous for its maple syrup & vast forests – is also among the most preferred overseas study destinations for Indians, and not without reason! Canada frequently features in the top 10 nations when it comes to quality of living. It is also home to some of the top colleges and universities in the world! Canada is also very welcoming and has a multicultural populace that helps Indian students feel more at home.</p>
<p>In addition to this, Canada has also launched the Student Partnership Program (SPP) to make it easier for Indian students to secure study visa.</p>
<h5>What is Canada Study work permit:</h5>
<p>It should be noted that different study permits may have different requirements. Almost all international students will need a study permit, which is the authorization to pursue a course of study within the nation. There are a few exceptions to this rule, though. If your course lasts less than six months, you do not need a study permit. You can enrol at Designated Learning Institutions after receiving the study permit (DLIs). An educational facility that is permitted to admit foreign students by the territory or provincial government is known as a DLI.</p>
<h5>What is SPP?</h5>
<p>The SPP was created by Citizenship and Immigration Canada (CIC) in collaboration with the Association of Canadian Community Colleges and debuted in 2009. (ACCC). The goal of this initiative is to make it easier for applicants who want to study at particular Canadian institutions to obtain visas. If you decide to attend one of the more than 50 member universities, you can apply through SPP and receive a quick and simple study visa.</p>
<p>Note: Once you got refusal in SDS category, you can apply in same college but your visa category will be NON SPP. You cannot reapply for the same SPP.</p>
<h5>What is Non-SPP?</h5>
<p>Colleges & universities participating under the SPP program accept Non-SPP (general) applicants as well. Therefore, if you have chosen a college that participates in the SPP program, you have the choice of applying as a Non-SPP applicant too!</p>
<h5>What is the difference between SPP and Non-SPP?</h5>
<p>The only main differences are the documents required & the ease of obtaining the visa. Students under the SPP require fewer documents than Non-SPP applicants. Moreover, the process under the SPP is much quicker and also much easier!</p>
<p>Documents required for SPP :</p>
<ul>
  <li>Letter of Acceptance from participating colleges under SPP</li>
  <li>IELTS score sheet with at least an overall band of 6.0 for undergraduate courses and an overall band of 6.5 with no less than 6.0 for postgraduate programs</li>
  <li>12th-grade mark sheet with at least above 55% score for undergraduate programs</li>
  <li>Bachelor’s degree or three to four years of Advance Diploma with a minimum of 50% or above, is required for postgraduate courses</li>
  <li>Proof of work experience</li>
  <li>Proof of payment of Tuition Fee</li>
  <li>Guaranteed Investment Certificate (GIC) of $10,000 for living expenses</li>
  <li>Medical test results conducted only by approved panel doctors</li>
  <li>Applicable Visa fees</li>
</ul>

<h5>Documents required for Non-SPP:</h5>
<p>To get into a college or university of one’s choice, Non-SPP applicants will have to submit all of the above documents except for the GIC of $10,000. In place of the GIC, they will have to provide proof of funds to support living expenses. This includes bank savings, investments, education loan, etc. In addition to their work experience letter, they will also have to provide the last 3 month’s play slips.</p>
<p>On June 8, 2018, Immigration, Refugee and Citizenship Canada has launched a new program namely Student Direct Stream (SDS) to make processing times faster for students from Antigua and Barbuda, Brazil, China, Colombia, Costa Rica, India, Morocco, Pakistan, Peru, Philippines, Senegal, Saint Vincent and the Grenadines, Trinidad and Tobago, and Vietnam. The SDS program is supposedly replacing SPP, the previous student visa program that also facilitated a faster visa process.</p>
<p>As per the newly introduced SDS program, students must meet a certain set of criteria, stricter than those compared to the erstwhile SPP.</p>
<h5>SDS:</h5>
<p>The Student Direct Stream (SDS) offers express entry to Canada quickly and directly. The visa application process will also be quicker. Several SDS colleges in Canada offer almost all top courses and specialisations.</p>
<h5>Non-SDS:</h5>
<p>Non-SDS is the general category for all types of visas for studying, working and residing in Canada. Those who missed their chance to apply through SDS can choose Non-SDS. You are not required to show the bank balance in your home country if you apply under this.</p>
<h5>University Category:</h5>
<p>The university category is different from Non-SDS in that you need to have an IELTS score of at least 6.0, which is not a requirement for Non-SDS.</p>
<h5>Canada Student Visa Fee:</h5>
<p>The cost of a student visa for Canada is 150 Canadian dollars. It should be noted that this is based on the current exchange rate, which is subject to change after a few days. In order to determine the precise rate in Indian Rupees, you must check the currency rate at the time of application.</p>


<h5>Canada Student Visa Requirements</h5>
<p>It is evident that you need to satisfy a few requirements to get a Canadian student visa. If you do not satisfy at least a single one of these requirements, your visa application may even be rejected. Thus, it is important to know the requirements in detail. They are listed below.</p>
<ul>
  <li>You should be accepted by a Designated Learning Institution (DLI).</li>
  <li>You need a clean criminal record and a police clearance certificate to prove it.</li>
  <li>You should prove that you have enough funds in your home country to support your educational and living expenses.</li>
  <li>You must have a medical certificate proving that you are medically fit.</li>
  <li>You must be fully vaccinated with COVISHIELD/AstraZeneca, Bharath Biotech Covaxin etc.</li>
</ul>
<h5>Documents Required for Canada Student Visa</h5>
<p>You must submit a few documents to apply for a Canadian student visa. It is important to apply for the visa early so that it gets approved on time. The documents required for a Canada student visa are explained below.</p>
<ul>
  <li>Acceptance letter from a recognised DLI: An acceptance letter from a recognised DLI is one of the most important documents you need to apply for a Canadian student visa.</li>
  <li>Passport: You need a valid passport, the expiry date of which should not fall during your stay in Canada. However, you also have the option to renew your passport if it expires during your study in Canada.</li>
  <li>Proof of funds: You need to prove that you have enough funds to cover your entire tuition and living expenses in Canada. Canadian Immigration expects a student to have CAD 10,000 every year.</li>
  <li>Immigration Medical Examination (IME): You need to prove that you are medically fit to move to Canada and study there. You should book an appointment, visit the listed doctors, and get yourself tested a week before the start of your visa application.</li>
  <li>Passport-size photographs: If you apply offline, you must have two passport-size photographs. For an online application, the size of the digital copy should not exceed 4MB. Other requirements include:
    <ul>
      <li>The photograph should not be older than six months.</li>
      <li>The size should be at least 35mm*45mm.</li>
      <li>Your name and date of birth must be written on the back of each photograph.</li>
      <li>You should have a neutral facial expression with no headgear except for religious reasons.</li>
      <li>The photograph should have a plain background.</li>
    </ul>
  </li>
  <li>Statement Of Purpose (SOP): An SOP is an essay that states the purpose of studying the course in Canada. A well-written SOP will enhance your chances of getting shortlisted for your preferred course.</li>
  <li>English proficiency test scores: You need to attend any English proficiency test like IELTS, TOEFL etc. and get an acceptable score.</li>
</ul>

<h5>Stay Back in Canada and Post-Graduation Work Permit (PGWP)</h5>
<p>Canadian government offers the option to stay back after your studies there. The rules pertaining to the same are given below.</p>
<ul>
  <li>Your course duration should be at least eight months.</li>
  <li>If you opt for a one-year program, you can stay back for one year.</li>
  <li>If you pursue a two-year program, you can stay back for up to three years after your course completion.</li>
  <li>You should apply for the post-study work permit within 180 days of the completion of your course.</li>
  <li>If your course duration is less than two years, you can stay back for the number of days equivalent to your course duration. For instance, if your course duration is 10 months, you can stay back for 10 months.</li>
</ul>





